// Language management
class LanguageManager {
  constructor() {
    this.currentLanguage = localStorage.getItem('language') || 'en';
    this.translations = translations;
    this.init();
  }

  init() {
    console.log('LanguageManager init called');
    console.log('Current language:', this.currentLanguage);
    this.updatePageDirection();
    this.translatePage();
    
    // Add a delay before setting up language toggle to ensure DOM is ready
    setTimeout(() => {
      console.log('Setting up language toggle after delay');
      this.setupLanguageToggle();
    }, 500);
  }

  getCurrentLanguage() {
    return this.currentLanguage;
  }

  setLanguage(lang) {
    console.log('Setting language to:', lang);
    this.currentLanguage = lang;
    localStorage.setItem('language', lang);
    console.log('Language saved to localStorage');
    this.updatePageDirection();
    this.translatePage();
    this.updateSlideshow();
    this.updateLanguageButtonStates();
    console.log('Language switch completed');
  }

  updatePageDirection() {
    document.documentElement.dir = this.currentLanguage === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = this.currentLanguage;
  }

  translate(key) {
    const keys = key.split('.');
    let value = this.translations[this.currentLanguage];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  }

  translatePage() {
    // Translate elements with data-translate attribute
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
      const key = element.getAttribute('data-translate');
      const translation = this.translate(key);
      
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translation;
      } else {
        element.textContent = translation;
      }
    });
    
    // Translate elements with data-translate-placeholder attribute
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(element => {
      const key = element.getAttribute('data-translate-placeholder');
      const translation = this.translate(key);
      element.placeholder = translation;
    });
  }

  updateSlideshow() {
    // Update slideshow with translated content
    if (window.slideshowData && typeof window.updateSlideContent === 'function') {
      const slides = this.translate('slideshow.slides');
      if (Array.isArray(slides)) {
        // Update the global slideshow data
        window.slideshowData = slides.map((slide, index) => ({
          image: window.slideshowData[index]?.image || `images/slide-${index + 1}.jpg`,
          title: slide.title,
          description: slide.description
        }));
        
        // Update current slide if slideshow is initialized
        if (window.currentSlide !== undefined) {
          window.updateSlideContent(window.currentSlide);
        }
      }
    }
  }

  setupLanguageToggle() {
    // Setup existing language toggles in header
    const desktopToggle = document.getElementById('language-toggle');
    const mobileToggle = document.getElementById('mobile-language-toggle');
    
    console.log('Setting up language toggles:');
    console.log('Desktop toggle found:', !!desktopToggle);
    console.log('Mobile toggle found:', !!mobileToggle);
    
    // Setup desktop toggle
    if (desktopToggle) {
      this.setupToggleButtons(desktopToggle);
    }
    
    // Setup mobile toggle
    if (mobileToggle) {
      this.setupToggleButtons(mobileToggle);
    }
  }
  
  setupToggleButtons(toggle) {
    const langButtons = toggle.querySelectorAll('.language-btn');
    
    console.log('Setting up toggle buttons for:', toggle.id);
    console.log('Language buttons found:', langButtons.length);
    
    // Set initial active state
    langButtons.forEach(btn => {
      const lang = btn.getAttribute('data-lang');
      console.log('Button language:', lang, 'Current language:', this.currentLanguage);
      if (lang === this.currentLanguage) {
        btn.classList.add('active');
        console.log('Setting active for:', lang);
      } else {
        btn.classList.remove('active');
      }
      
      // Remove any existing event listeners (to prevent duplicates)
      const newBtn = btn.cloneNode(true);
      btn.parentNode.replaceChild(newBtn, btn);
      
      // Add event listeners with direct function binding
      const self = this; // Store reference to LanguageManager instance
      newBtn.onclick = function(e) {
        e.preventDefault();
        console.log('Language button clicked');
        const lang = this.getAttribute('data-lang');
        console.log('Switching to language:', lang);
        if (lang) {
          self.setLanguage(lang);
          
          // Update active state for all toggles
          self.updateLanguageButtonStates();
        }
      };
      console.log('Added click handler to button:', lang);
    });
  }
  
  updateLanguageButtonStates() {
    console.log('Updating language button states');
    const allLangButtons = document.querySelectorAll('.language-btn');
    console.log('Found language buttons:', allLangButtons.length);
    
    allLangButtons.forEach(btn => {
      const lang = btn.getAttribute('data-lang');
      console.log('Button language:', lang, 'Current language:', this.currentLanguage);
      
      if (lang === this.currentLanguage) {
        console.log('Adding active class to:', lang);
        btn.classList.add('active');
      } else {
        console.log('Removing active class from:', lang);
        btn.classList.remove('active');
      }
    });
  }
}

// Make LanguageManager available globally
if (typeof window !== 'undefined') {
  window.LanguageManager = LanguageManager;

  // Add a global switchLanguage function for direct button access
  window.switchLanguage = function(lang) {
    try {
      console.log('Global switchLanguage called with:', lang);
      if (window.languageManager) {
        console.log('Using existing languageManager instance');
        window.languageManager.setLanguage(lang);
      } else {
        console.log('Creating new LanguageManager instance');
        window.languageManager = new LanguageManager();
        window.languageManager.setLanguage(lang);
      }
      console.log('Language switch completed successfully');
    } catch (error) {
      console.error('Error in switchLanguage function:', error);
    }
  };

  // Auto-initialize LanguageManager when DOM is ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function() {
      console.log('Auto-initializing LanguageManager on DOMContentLoaded');
      if (!window.languageManager) {
        window.languageManager = new LanguageManager();
      }
    });
  } else {
    // DOM is already ready
    console.log('Auto-initializing LanguageManager immediately');
    if (!window.languageManager) {
      window.languageManager = new LanguageManager();
    }
  }
}

